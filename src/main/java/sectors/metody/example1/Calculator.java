package sectors.metody.example1;

public class Calculator {
    double add(double x, double y){
        return x+y;
    }
    double substract(double x, double y){
        return x-y;
    }
    double multiply(double x, double y){
        return x*y;
    }
    double divide(double x, double y){
        return x/y;
    }
}
