package sectors.metody.example5;

public class Translate {
    double metersToCentymeters(double metr){
        return metr*100;
    }
    double metersToMilimeters(double metr){
        return metr*1000;
    }
    double centymetersToMeters(double centymeters){
        return centymeters/100;
    }
    double milimetersToMeters(double milimeters){
        return milimeters/1000;
    }
}
