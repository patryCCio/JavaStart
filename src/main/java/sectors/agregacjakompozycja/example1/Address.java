package sectors.agregacjakompozycja.example1;

public class Address {
    String city;
    String street;
    int home;
    int flat;
    String postalCode;

    public Address(String city, String street, int home, int flat, String postalCode) {
        this.city = city;
        this.street = street;
        this.home = home;
        this.flat = flat;
        this.postalCode = postalCode;
    }
}
